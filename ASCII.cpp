#include <iostream>
using namespace std;
 int main() {
 	char n;
 	cout << "Enter a character : ";
    cin >> n;
    
    cout << "ASCII value : " << ((int)n) << endl;
    return 0;
    
 }
 // ((int)n) : Converts the character into its assigned digit.(In ASCCI table).
 // ASCII : Stands for (American Standard Code For Information Interchange).
