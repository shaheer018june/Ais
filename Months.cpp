#include <iostream>
using namespace std;
int main() {
    int month;
    cout << "Enter Month: ";
    cin >> month;
    cout << "Month : " ;
    switch (month) {
    case 01:
    cout << "January";
    break;
    case 02:
    cout << "February";
    break;
    case 03:
    cout << "March";
    break;
    case 04:
    cout << "April";
    break;
    case 05:
    cout << "May";
    break;
    case 06:
    cout << "June";
    break;

    }
    return 0;
}
     
